package chessgame.model;

/**
 * The Square enum represents the possible states of a chess square.
 */
public enum Square {

    /**
     * Represents an empty square on the chess board.
     */
    NONE,

    /**
     * Represents a square occupied by a black chess piece.
     */
    BLACK,

    /**
     * Represents a square occupied by a white chess piece.
     */
    WHITE
}
